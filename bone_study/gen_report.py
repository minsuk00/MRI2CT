def img(path, w="100%"):
    if not os.path.exists(path):
        return f"<div class='missing'>[missing fig: {os.path.basename(path)}]</div>"
    b = base64.b64encode(open(path, "rb").read()).decode()
    return f"<img style='width:{w};max-width:1100px;display:block;margin:8px auto' src='data:image/png;base64,{b}'/>"


def load(name):
    p = f"{BS}/{name}"
    return np.load(p, allow_pickle=True).item() if os.path.exists(p) else None


M = ["amp_bias", "amp_scatter", "edge_sharp", "bone_mae", "soft_mae"]
MH = ["amp bias", "amp scatter", "edge sharp", "bone MAE", "soft MAE"]


def row(lab, m, bold=False):
    def cell(k):
        v = m[k]
        s = f"{v:+.2f}" if k == "amp_bias" else (f"{v:.2f}" if k in ("amp_scatter", "edge_sharp") else f"{v:.3f}")
        return f"<td>{s}</td>"
    c = "".join(cell(k) for k in M)
    cls = " class='hl'" if bold else ""
    return f"<tr{cls}><td class='lab'>{lab}</td>{c}</tr>"


def table(rowdict, order=None, bold=None):
    head = "".join(f"<th>{h}</th>" for h in MH)
    order = order or list(rowdict.keys())
    bold = bold or []
    body = "".join(row(k, rowdict[k], k in bold) for k in order)
    return (f"<table><thead><tr><th>variant</th>{head}</tr></thead><tbody>{body}</tbody></table>"
            "<div class='note'>amp bias &lt;0 = undershoot; edge sharp 1.0 = real-CT-sharp; lower MAE better.</div>")


